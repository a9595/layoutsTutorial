package com.example.layouts;

public class Student {
	public int Id;
	public String Name;
	public String Surname;

	public Student() {
		Id = -1;
		Name = null;
		Surname = null;
	}
	
	public Student(int id, String name, String surname) {
		super();
		Id = id;
		Name = name;
		Surname = surname;
	}

	
}
